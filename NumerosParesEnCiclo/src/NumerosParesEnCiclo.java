/**
 * Diseñe un programa que muestre los numeros pares entre el 1 y el 100
 */
 
public class NumerosParesEnCiclo {
    
    public static void main(String[] args) {
    	int i;
        
        for (i=1;i<=100;i++){
        	if (i%2==0){
        		System.out.println("" +i);
        	}
        }
    	
    }
}
